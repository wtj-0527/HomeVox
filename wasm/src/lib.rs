pub fn marching_cubes_3d(
    data: &[f32],
    nx: usize,
    ny: usize,
    nz: usize,
    iso_level: f32,
) -> Vec<f32> {
    if nx < 2 || ny < 2 || nz < 2 {
        return vec![];
    }
    let Some(vertex_count) = nx.checked_mul(ny).and_then(|n| n.checked_mul(nz)) else {
        return vec![];
    };
    if !iso_level.is_finite() || data.len() < vertex_count || data[..vertex_count].iter().any(|value| !value.is_finite()) {
        return vec![];
    }
    let mut vertices: Vec<f32> = Vec::new();
    for z in 0..nz - 1 {
        for y in 0..ny - 1 {
            for x in 0..nx - 1 {
                let mut corner_values = [0.0f32; 8];
                let mut corner_positions = [Vec3::new(0.0, 0.0, 0.0); 8];
                for ci in 0..8 {
                    let (dx, dy, dz) = CORNER_OFFSETS[ci];
                    let ix = x + dx;
                    let iy = y + dy;
                    let iz = z + dz;
                    let idx = ix + iy * nx + iz * nx * ny;
                    corner_values[ci] = data[idx];
                    corner_positions[ci] = Vec3::new(ix as f32, iy as f32, iz as f32);
                }
                let mut cube_index: usize = 0;
                for ci in 0..8 {
                    if corner_values[ci] >= iso_level {
                        cube_index |= 1 << ci;
                    }
                }
                let edge_mask = EDGE_TABLE[cube_index];
                if edge_mask == 0 {
                    continue;
                }
                let mut edge_vertices = [Vec3::new(0.0, 0.0, 0.0); 12];
                for e in 0..12 {
                    if (edge_mask >> e) & 1 != 0 {
                        let (c0, c1) = EDGE_ENDPOINTS[e];
                        let v0 = corner_values[c0];
                        let v1 = corner_values[c1];
                        let t = if (v1 - v0).abs() < 1e-7 {
                            0.5
                        } else {
                            (iso_level - v0) / (v1 - v0)
                        };
                        edge_vertices[e] =
                            Vec3::lerp(&corner_positions[c0], &corner_positions[c1], t);
                    }
                }
                let tri_entry = &TRI_TABLE[cube_index];
                for tri_edges in tri_entry.chunks(3) {
                    if tri_edges.len() < 3 || tri_edges[0] < 0 {
                        break;
                    }

                    // TRI_TABLE winding is used with the positive-inside convention below
                    // (`corner_value >= iso_level`); keep the table order so normals point outward.
                    for &ei in [tri_edges[0], tri_edges[1], tri_edges[2]].iter() {
                        let v = &edge_vertices[ei as usize];
                        vertices.push(v.x);
                        vertices.push(v.y);
                        vertices.push(v.z);
                    }
                }
            }
        }
    }
    vertices
}

