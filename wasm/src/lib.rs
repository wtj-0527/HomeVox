    fn make_sphere(nx: usize, ny: usize, nz: usize, cx: f32, cy: f32, cz: f32, r: f32) -> Vec<f32> {
        let mut data = vec![0.0f32; nx * ny * nz];
        for zi in 0..nz {
            for yi in 0..ny {
                for xi in 0..nx {
                    let dx = xi as f32 - cx;
                    let dy = yi as f32 - cy;
                    let dz = zi as f32 - cz;
                    let dist = (dx * dx + dy * dy + dz * dz).sqrt();
                    data[xi + yi * nx + zi * nx * ny] = r - dist;
                }
            }
        }
        data
    }

    #[test]
    fn test_sphere_4x4x4() {
        let field = make_sphere(4, 4, 4, 1.5, 1.5, 1.5, 1.2);
        let vertices = marching_cubes_3d(&field, 4, 4, 4, 0.0);
        assert!(!vertices.is_empty(), "should have triangles");
        assert_eq!(vertices.len() % 9, 0, "vertex count must be multiple of 9");
        let tri_count = vertices.len() / 9;
        assert!(
            tri_count >= 4,
            "at least a few triangles, got {}",
            tri_count
        );
        assert!(tri_count <= 80, "under cell_count*2, got {}", tri_count);
        for chunk in vertices.chunks(3) {
            assert!(chunk[0] >= 0.0 && chunk[0] <= 3.0);
            assert!(chunk[1] >= 0.0 && chunk[1] <= 3.0);
            assert!(chunk[2] >= 0.0 && chunk[2] <= 3.0);
        }
    }

