const fn build_edge_table() -> [u16; 256] {
    let mut table = [0u16; 256];
    let mut config: usize = 0;
    while config < 256 {
        let mut mask: u16 = 0;
        let mut e: usize = 0;
        while e < 12 {
            let (a, b) = EDGE_ENDPOINTS[e];
            if ((config >> a) & 1) != ((config >> b) & 1) {
                mask |= 1 << e;
            }
            e += 1;
        }
        table[config] = mask;
        config += 1;
    }
    table
}
