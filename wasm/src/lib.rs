#[wasm_bindgen]
pub fn get_mc_info(nx: usize, ny: usize, nz: usize) -> String {
    let vertices = nx
        .checked_mul(ny)
        .and_then(|n| n.checked_mul(nz))
        .unwrap_or(0);
    let cells = if nx < 2 || ny < 2 || nz < 2 {
        0
    } else {
        (nx - 1)
            .checked_mul(ny - 1)
            .and_then(|n| n.checked_mul(nz - 1))
            .unwrap_or(0)
    };
    format!(
        "Grid: {}x{}x{} ({} vertices), {} cells, est. {} triangles",
        nx,
        ny,
        nz,
        vertices,
        cells,
        cells.saturating_mul(2)
    )
}

